a = int(input("Digite um valor: "))
b = int(input("Digite outro valor: "))

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b
exponenciacao = a ** b
Resultado = ("soma: {}. \nsubtração: {}. \nmultiplicação: {}. \ndivisão: {}. \nresto: {} \nExponenciação: {} .".format(soma,subtracao,multiplicacao,divisao,resto,exponenciacao))
print(Resultado)
